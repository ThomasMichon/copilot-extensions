    def local_index(self):
        for i, (label, m, e, ok) in enumerate(self.machines):
            if (m, e) == self.src.LOCAL:
                return i
        return 1 if len(self.machines) > 1 else 0

    # ---- model helpers ----
    def is_all(self):
        return self.machines[self.machine_idx][1] is None

    # ---- registered-pivot task data (read-only, background-loaded) ----
    def _task_state(self):
        """(state, rows, error) for the current registered pivot at the current
        machine. ``state`` is idle|loading|ready|error. Triggers a background
        fetch of the pivot's ``list`` CLI on first view of a machine."""
        reg = self._reg_pivot()
        if reg is None:
            return ("idle", [], "")
        machine = self._pivot_machine()
        rt = self._pivot_runtime(reg)
        rt.ensure(machine)
        return rt.get(machine)

    def _task_rows(self):
        """The flat, selectable list of task entries (dicts) for the current
        registered pivot -- the ('T', i) stops index into this."""
        rows = self._task_state()[1]
        return rows if isinstance(rows, list) else []

    def _task_groups(self):
        """Task rows grouped by the pivot's worktree field for display. Returns
        ``[(group_label, [(row_index, entry), ...]), ...]`` in first-seen order."""
        reg = self._reg_pivot()
        rows = self._task_rows()
        if reg is None:
            return []
        groups: dict[str, list] = {}
        order: list[str] = []
        for i, r in enumerate(rows):
            wt = r.get(reg.worktree_field) if reg.worktree_field else None
            key = wt or "· unpinned"
            if key not in groups:
                groups[key] = []
                order.append(key)
            groups[key].append((i, r))
        return [(k, groups[k]) for k in order]

    def _selected_task(self):
        """The task entry under the ('T', i) cursor, or None."""
        if self.sel[0] != "T":
            return None
        rows = self._task_rows()
        i = self.sel[1]
        return rows[i] if 0 <= i < len(rows) else None

    def cur_machine(self):
        label, m, e, ok = self.machines[self.machine_idx]
        return m, e, ok

    def _scope_data(self):
        """Worktrees in the active machine-tab scope (All = every ready env)."""
        if self.is_all():
            ready = self.ready_envs()
            return [w for w in self.data if (w["machine"], w["env"]) in ready]
        m, e, _ = self.cur_machine()
        return [w for w in self.data if w["machine"] == m and w["env"] == e]

    def _hidden_count(self):
        """How many bridge/system worktrees are hidden in the current scope."""
        return sum(1 for w in self._scope_data() if w.get("hidden"))

    def _visible_scope_data(self):
        data = self._scope_data()
        if not self.show_hidden:
            data = [w for w in data if not w.get("hidden")]
        return data

    def current_list(self):
        """(cols, [(section_label, [records])]) for the selected machine tab.
        'All' interleaves every READY machine and shows the machine/env columns;
        a specific machine hides them (implied by the tab). Bridge/system
        worktrees are hidden unless Toggle-hidden is on (#1422). Worktrees with
        no owning Copilot session are split into a distinct 'Unowned' section so
        selecting one never silently cold-starts a blank conversation (#1026)."""
        cols = ACTIVE_SPECS if self.is_all() else LIST_SPECS
        a, r, c = self.src.bucket(self._visible_scope_data())
        unowned = [w for w in r if w.get("sessionless")]
        if unowned:
            r = [w for w in r if not w.get("sessionless")]
            return cols, [
                ("Active", a), ("Recent", r), ("Completed", c),
                ("Unowned · no prior session (Open starts fresh)", unowned),
            ]
        return cols, [("Active", a), ("Recent", r), ("Completed", c)]

    def list_records(self):
        _cols, secs = self.current_list()
        out = []
        for _label, rows in secs:
            out.extend(rows)
        return out

    def create_target(self):
        """Where '+ New Worktree' lands: the active machine, or LOCAL when on
        'All'."""
        if self.is_all():
            return self.src.LOCAL
        m, e, _ = self.cur_machine()
        return (m, e)

    def _update_actionable(self) -> bool:
        """True when the update indicator is a focusable action (#1430).

        Only when an update is *staged* (``available``) is there something to do
        (Enter = apply + restart); the spinner/✓ states are informational.
        """
        return getattr(self, "update_state", "idle") == "available"

    def _v_stops(self):
        """Top (View) region stops: the view pivot, the refresh icon ("V", 1)
        when an update is staged, then the ⚙ Configuration entry ("CFG", 0) when
        any pivot is hosted there (#1426)."""
        out = [("V", 0)]
        if self._update_actionable():
            out.append(("V", 1))
        if self._config_pivots():
            out.append(("CFG", 0))
        return out

    def stops(self):
        """Vertical Up/Down flow. ("V",0)=View nav, ("V",1)=update refresh icon
        (only when an update is staged), ("M",0)=machine picker,
        ("BTN",0)=button row, then the table/grid rows."""
        if self._kind() == "worktrees":
            out = [*self._v_stops(), ("M", 0), ("BTN", 0)]
            for i in range(len(self.list_records())):
                out.append(("L", i))
        elif self._kind() == "maintenance":
            out = [*self._v_stops(), ("M", 0), ("BTN", 0)]
            groups = self.maint_groups()
            if any(rows for _st, rows in groups):
                out.append(("SA", 0))   # the "Select all" checkbox
            li = 0
            for gi, (_st, rows) in enumerate(groups):
                out.append(("GH", gi))
                for _ in rows:
                    out.append(("C", li))
                    li += 1
        elif self._kind() == "registered":
            # Registered pivot: View nav + machine sub-nav, then one stop per
            # task row (grouped rows are flattened in first-seen order).
            out = [*self._v_stops(), ("M", 0)]
            for i in range(len(self._task_rows())):
                out.append(("T", i))
        else:
            out = [*self._v_stops()]
            for i in range(len(self.targets)):
                out.append(("PR", i))
            out.append(("BTN", 0))   # Apply/Reset at the bottom
        return out

    def _pr_head(self):
        """Grid-region entry point, restoring the last-focused row (#1288).
        pcol (the host column) already persists across Tab in/out."""
        return ("PR", min(self.last_pr, max(0, len(self.targets) - 1)))

    def region_head(self, zone):
        if zone == "PR":
            return self._pr_head()
        return {"V": ("V", 0), "CFG": ("CFG", 0), "M": ("M", 0),
                "BTN": ("BTN", 0), "L": ("L", 0), "SA": ("SA", 0),
                "GH": ("GH", 0), "C": ("C", 0), "PR": ("PR", 0)}.get(
                    zone, ("V", 0))

    def region_heads(self):
        """Tab/Shift+Tab jump targets — the entry point of each major region.
        The ⚙ Configuration entry joins the cycle (right after the View pivot)
        whenever a pivot is hosted there, so Tab reaches it (#1426)."""
        v = [("V", 0)]
        if self._config_pivots():
            v.append(("CFG", 0))
        if self._kind() == "worktrees":
            heads = [*v, ("M", 0), ("BTN", 0)]
            if self.list_records():
                heads.append(("L", 0))
        elif self._kind() == "maintenance":
            heads = [*v, ("M", 0), ("BTN", 0)]
            if self.maint_records():
                heads.append(("SA", 0))   # Tab into the selection/list region
        elif self._kind() == "registered":
            heads = [*v, ("M", 0)]
            if self._task_rows():
                heads.append(("T", 0))
        else:
            heads = [*v, self._pr_head(), ("BTN", 0)]
        return heads

    def default_sel(self):
        kind = self._kind()
        d = {"worktrees": ("BTN", 0), "maintenance": ("BTN", 0),
             "profiles": ("PR", 0), "registered": ("M", 0)}.get(kind, ("BTN", 0))
        # The Profiles default (``("PR", 0)``) is only valid when there is at
        # least one target row. With no targets (or no host columns) that stop
        # doesn't exist, so fall back to the first real stop instead of handing
        # back a phantom the caller would ``stops.index()`` on (ValueError).
        stops = self.stops()
        if d not in stops:
            return stops[0] if stops else d
        return d

    def anchors(self):
        return self.region_heads()

    def cleanup_rows(self):
        """Maintenance worktree list, scoped to the active machine sub-pivot
        (All = every machine), each tagged with its prune disposition.

        Shows every in-scope worktree -- including in-use/work-bearing ones --
        so the disposition column is honest; the Cleanup dialog only *offers*
        the cleanable buckets, and the executor re-checks safety per worktree.
        """
        rows = self._visible_scope_data()
        for w in rows:
            bucket = w.get("cleanup_bucket", "wip")
            lvl = derive.BUCKET_DISPO.get(bucket, "")
            txt = derive.BUCKET_REASON.get(bucket, "")
            # Blended disposition (verdict + reason in one colored chip).
            w["dispo_level"] = lvl
            w["dispo"] = f" {DISPO_MARK[lvl]} {txt}" if lvl else ""
        return rows

    # ---- Maintenance grouping + multi-select (#1345) ----
    def maint_groups(self):
        """Maintenance rows grouped by display state, in MAINT_GROUP_ORDER."""
        rows = self.cleanup_rows()
        by = {}
        for r in rows:
            by.setdefault(r.get("state", "?"), []).append(r)
        groups = [(st, by.pop(st)) for st in MAINT_GROUP_ORDER if st in by]
        groups.extend(by.items())   # any unlisted states, first-seen order
        return groups

    def maint_records(self):
        """Flat Maintenance row list in grouped display order (indexes the
        ("C", i) stops -- must match build_body's ordering)."""
        out = []
        for _st, rows in self.maint_groups():
            out.extend(rows)
        return out

    def _maint_ids(self):
        return {r["id4"] for r in self.maint_records()}

    def _toggle_maint(self, i):
        recs = self.maint_records()
        if 0 <= i < len(recs):
            wid = recs[i]["id4"]
            self.maint_sel.symmetric_difference_update({wid})
            self.debug = (f"{'selected' if wid in self.maint_sel else 'deselected'}"
                          f" {wid} · {len(self.maint_sel)} selected")

    def _toggle_maint_all(self):
        ids = self._maint_ids()
        if ids and ids <= self.maint_sel:
            self.maint_sel -= ids
            self.debug = "cleared selection"
        else:
            self.maint_sel |= ids
            self.debug = f"selected all · {len(ids)}"

    def _toggle_group(self, gi):
        groups = self.maint_groups()
        if 0 <= gi < len(groups):
            st, rows = groups[gi]
            ids = {r["id4"] for r in rows}
            if ids and ids <= self.maint_sel:
                self.maint_sel -= ids
                self.debug = f"deselected {st} · {len(ids)}"
            else:
                self.maint_sel |= ids
                self.debug = f"selected {st} · {len(ids)}"

    def _open_maint_menu(self):
        """Open the Maintenance actions menu for the selected set; if nothing
        is selected, select the focused row and open it for that one (#1345)."""
        if not self.maint_sel:
            rec = self._selected_record()
            if not rec:
                return
            self.maint_sel = {rec["id4"]}
        ids = set(self.maint_sel)
        chosen = [r for r in self.maint_records() if r["id4"] in ids]
        acts = []
        if any(r.get("ff_eligible") for r in chosen):
            acts.append("Sync")
        if any(self._cleanable(r) for r in chosen):
            acts.append("Cleanup")
        if not acts:
            # Nothing actionable for this selection (no FF-eligible or cleanable
            # worktree) -- don't open an empty menu.
            self.debug = (f"no maintenance action for {len(chosen)} "
                          f"selected worktree(s)")
            return
        self.maint_menu = {"ids": ids, "actions": acts, "count": len(chosen)}
        self.maint_menu_idx = 0

    def _key_maint_menu(self, key):
        m = self.maint_menu
        acts = m["actions"]
        if key == "down":
            self.maint_menu_idx = (self.maint_menu_idx + 1) % len(acts)
        elif key == "up":
            self.maint_menu_idx = (self.maint_menu_idx - 1) % len(acts)
        elif key == "enter":
            act = acts[self.maint_menu_idx]
            ids = m["ids"]
            self.maint_menu = None
            if act == "Sync":
                self._open_sync(ids=ids)
            elif act == "Cleanup":
                self._open_cleanup(ids=ids)
        elif key in ("escape", "q", "tab"):
            self.maint_menu = None

    # ---- Profiles matrix helpers ----
    def profile_col_widths(self):
        return [max(len(f"{m} {e}"), 3) + 2 for _lbl, m, e in self.host_cols]

    def _host_header_cell(self, j, colw):
        """One Profiles host-column header: machine (config) name in the header
        slot color + env in its per-env color, centered to the column width.
        The active host (the column being edited) gets the SAME subtle shading
        the machine tabs use for an active-but-unfocused tab -- never the
        inversion cursor, since the real cursor lives in the grid cell."""
        _lbl, m, e = self.host_cols[j]
        active = j == self.pcol
        unavail = j in self._prof_unavailable
        name_base = C_DISABLED if unavail else ("bold white" if active else C_TABOFF)
        env_base = C_DISABLED if unavail else C_ENV.get(e, name_base)
        cell = Text()
        cell.append(m, style=self._hl(name_base, active, False))
        cell.append(" ", style=self._hl("", active, False))
        cell.append(e, style=self._hl(env_base, active, False))
        pad = max(0, colw - cell.cell_len)
        left = pad // 2
        out = Text(" " * left)
        out.append_text(cell)
        out.append(" " * (pad - left))
        return out

    def profiles_present(self):
        return sum(1 for v in self.grid.values() if v)

    # ---- Profiles: real per-host column load + Apply (own-column model) ----
    def _target_sel(self, ti):
        """The TargetSel a target row represents (kind = agent|shell)."""
        t = self.targets[ti]
        return profiles_mod.TargetSel(
            t["machine"], t["env"], "agent" if t["agent"] else "shell")

    def _start_profile_load(self):
        """Background-load every host's saved column into the grid.

        Read-only: each host's column is fetched (local in-process, remote over
        SSH) and projected onto the grid; ``applied`` is snapshotted to match so
        the freshly-loaded state reads as 'no pending changes'.
        """
        self._prof_loading = True

        def work():
            from . import profiles_io
            cols = {}
            for hi, (_lbl, hm, he) in enumerate(self.host_cols):
                try:
                    cols[hi] = self._prof_load(hm, he)
                except Exception:
                    cols[hi] = None   # treat a load failure as legacy (all-on)
            with self._prof_lock:
                # Don't clobber edits the user already started before the load
                # resolved -- only project columns onto a pristine grid.
                if not self.grid_dirty():
                    unavail = set()
                    for hi, sels in cols.items():
                        if sels is profiles_io.UNAVAILABLE:
                            # Unreachable / too-old remote: we can't know its
                            # real column, so mark it read-only. Seed just the
                            # locked self-diagonal so the grid stays consistent;
                            # cells render as "?" and are never editable or
                            # applied (#1370).
                            unavail.add(hi)
                            for ti in range(len(self.targets)):
                                self.grid[(ti, hi)] = self.cell_locked(ti, hi)
                            continue
                        if sels is None:
                            # Legacy/unmanaged host: every target is "on" (the
                            # mirror's historical emit-everything behavior), so
                            # the grid matches reality until the user prunes.
                            for ti in range(len(self.targets)):
                                self.grid[(ti, hi)] = True
                            continue
                        keys = {s.key for s in sels}
                        for ti in range(len(self.targets)):
                            on = self._target_sel(ti).key in keys or \
                                self.cell_locked(ti, hi)
                            self.grid[(ti, hi)] = on
                    self._prof_unavailable = unavail
                    self.applied = dict(self.grid)
                self._prof_loading = False
                self._prof_loaded = True

        threading.Thread(target=work, name="profiles-load", daemon=True).start()

    def _column_sels(self, hi):
        """Build the TargetSel list for host column ``hi`` from the grid."""
        out = []
        for ti in range(len(self.targets)):
            if self.grid.get((ti, hi)):
                out.append(self._target_sel(ti))
        return out

    def _apply_profiles(self):
        """Open the Apply confirmation: list exactly which terminal profiles
        each changed host column will gain/lose, *before* touching anything.

        Confirming runs the per-host progress dialog (local writes mirror to the
        terminal profiles, remote writes go over SSH). This is destructive to
        the terminal app's profile list, so the confirm step (and its explicit
        add/remove diff) is mandatory -- the regeneration is not silent.
        """
        if not callable(self._prof_apply):
            # Mock source (fixtures/tests without the IO hook): instant.
            self.applied = dict(self.grid)
            self.debug = f"Applied · {self.profiles_present()} profiles (mock)"
            return
        changed = []
        diffs = {}
        for hi in range(len(self.host_cols)):
            if hi in self._prof_unavailable:
                continue             # read-only column: never diffed or applied
            added, removed = [], []
            for ti in range(len(self.targets)):
                now = bool(self.grid.get((ti, hi)))
                was = bool(self.applied.get((ti, hi)))
                if now != was:
                    (added if now else removed).append(self._target_sel(ti))
            if added or removed:
                changed.append(hi)
                diffs[hi] = (added, removed)
        if not changed:
            self.debug = "Apply · nothing changed"
            return
        self.prof_confirm = {"changed": changed, "diffs": diffs, "idx": 0}

    def _start_profiles_run(self):
        """Execute the confirmed Apply: one progress item per changed host."""
        from . import maintenance
        changed = self.prof_confirm["changed"]
        diffs = self.prof_confirm.get("diffs", {})
        n_add = sum(len(diffs[hi][0]) for hi in changed if hi in diffs)
        n_rem = sum(len(diffs[hi][1]) for hi in changed if hi in diffs)
        self.prof_confirm = None
        items, tasks = [], []
        for hi in changed:
            _lbl, hm, he = self.host_cols[hi]
            key = f"{hm}/{he}"
            items.append({"id4": key, "title": f"{hm} {he}",
                          "machine_env": f"{hm} {he}", "hi": hi,
                          "state": "pending"})
            tasks.append((key, self._make_apply_task(hm, he, hi)))

        self.executor = maintenance.MaintenanceExecutor("profiles", tasks)
        self.progress = {
            "verb": "Apply profiles", "op": "profiles",
            "scope": f"{len(changed)} host column(s)",
            "items": items, "recs": [], "picked": [],
            "ticks": 0, "steps": 3, "done": False, "armed": True,
            "include_unused": False, "include_conversations": False,
            "n_add": n_add, "n_rem": n_rem,
        }
        self.executor.start()

    def _key_prof_confirm(self, key):
        if key in ("enter", "space"):
            self._start_profiles_run()
        elif key in ("escape", "q"):
            self.prof_confirm = None
            self.debug = "Apply cancelled"

    def _make_apply_task(self, machine, env, hi):
        """A progress task that writes one host column; ``ok`` drives ✓/✗."""
        sels = self._column_sels(hi)

        def _run():
            ok, detail = self._prof_apply(machine, env, sels)
            return {"ok": bool(ok), "reason": detail, "detail": detail}
        return _run

    def _commit_applied_profiles(self):
        """After an Apply run, advance ``applied`` for every host that succeeded
        (matched by the executor's per-item DONE state)."""
        p = self.progress
        if not p or p.get("op") != "profiles":
            return
        for it in p["items"]:
            if it.get("state") == "done":
                hi = it["hi"]
                for ti in range(len(self.targets)):
                    self.applied[(ti, hi)] = self.grid.get((ti, hi), False)

